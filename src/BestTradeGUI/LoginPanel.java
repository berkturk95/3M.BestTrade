    private void signInButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_signInButtonMouseClicked
        // TODO add your handling code here:

        String username = userNameField.getText();
        String password = new String(passwordField.getPassword());
        try {
            client =  database.login(username, password);
        } catch (IOException ex) {
            Logger.getLogger(LoginPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        items = database.getItems();
        if( client != null){
            
            TradeScreen TradeScreen = new TradeScreen(database);
            UIManager.frame.add(TradeScreen);
            
           // frame.getContentPane().removeAll();
           // frame.getContentPane().add(tradeScreen);
            setVisible(false);
            TradeScreen.setVisible(true);

        }
        else{
            System.out.println("HATA");
            JOptionPane.showMessageDialog(null, "Incorrect username or password", "SIGN IN ERROR" , JOptionPane.INFORMATION_MESSAGE);
            //Pop up yapmak lazÄ±m
        }

    }//GEN-LAST:event_signInButtonMouseClicked

